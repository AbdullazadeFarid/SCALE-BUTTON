
function scaleIn() {
    var element = document.getElementById('myElement');
    element.style.transition = 'transform 0.3s ease';
    element.style.transform = 'scale(1.2)';


  }



  function scaleOut() {
    var element = document.getElementById('myElement');
    element.style.transition = 'transform 0.3s ease';
    element.style.transform = 'scale(1)';
  }